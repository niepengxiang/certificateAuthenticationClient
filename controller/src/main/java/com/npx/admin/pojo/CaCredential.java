package com.npx.admin.pojo;

public class CaCredential {
	
	private String authSrcId;
	private String reqOpt;
	private String certChainId;
	private String cerChainFile;
	
	
	public String getAuthSrcId() {
		return authSrcId;
	}
	public void setAuthSrcId(String authSrcId) {
		this.authSrcId = authSrcId;
	}
	public String getReqOpt() {
		return reqOpt;
	}
	public void setReqOpt(String reqOpt) {
		this.reqOpt = reqOpt;
	}
	public String getCertChainId() {
		return certChainId;
	}
	public void setCertChainId(String certChainId) {
		this.certChainId = certChainId;
	}
	public String getCerChainFile() {
		return cerChainFile;
	}
	public void setCerChainFile(String cerChainFile) {
		this.cerChainFile = cerChainFile;
	}
	
	
	
}
